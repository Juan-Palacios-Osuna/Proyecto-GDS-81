    private void modificarBebida(BebidaModel bebida){
        //int id = evento.getId();
		
		//Modificar el evento
		try {
                            String queryModificarPro = "UPDATE bebidas SET nombre = ? , denominacion = ?, registro_disponible = ? WHERE id_bebida = ?";

			PreparedStatement execQuery = conn.prepareStatement(queryModificarPro);
			execQuery.setString(1, bebida.getNombre());
                        execQuery.setInt(2, bebida.getDenominacion());
			execQuery.setBoolean(3, bebida.getDisponible());
			execQuery.setInt(4, bebida.getId());

			execQuery.executeUpdate();

			//Mostrar mensaje
			JOptionPane.showMessageDialog(null, "Bebida ha sido modificada.");
                        
                        this.cambio = true;
		} catch (SQLException errorMod) {
			JOptionPane.showMessageDialog(null, "Error al modificar la bebida. \n" + errorMod, "Error", JOptionPane.ERROR_MESSAGE);
		}
    }
